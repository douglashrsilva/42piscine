#include <unistd.h>

void	ft_print_comb2(void)
{
	char	i[5];
	int		x;
	int		y;

	x = 0;
	while (x <= 98)
	{
		y = x + 1;
		while (y <= 99)
		{
			i[0] = (x / 10) + '0';
			i[1] = (x % 10) + '0';
			i[2] = ' ';
			i[3] = (y / 10) + '0';
			i[4] = (y % 10) + '0';
			write (1, &i, 5);
			if (x != 98 || y != 99)
			{
				write (1, ", ", 2);
			}
			y++;
		}
		x++;
	}
}
